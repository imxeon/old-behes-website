  <h1>WHAT IS BEHES ?</h1>
  <p>
 Behes is a non-profit school debate platform connecting over 900 schools spanning 40 cities from five countries.  Every Behes tournament is a celebration of ideas, with students exploring a wide variety of topics and critically examining them. Designed to be interactive and challenging, each tournament rewards the top teams with qualification to the Maha Behes, our season finals held in Indiaâ€™s national capital every summer.
</p>
<p>
All the debate topics at our events are given impromptu and based on a themed suggested reading (and viewing) list that is updated every year. Students explore selections spanning a variety of disciplines in their bid to challenge the best in the debating community. The free-flowing debate format encourages growth of research, collaborative and analytical apart from oratory skills. Behes has now impacted over ten thousand students, some of them as young as nine years old.
</p>
<p>
We also offer workshops at gracious host schools (usually preceding tournaments) where we demonstrate How to 'Behes'. If you like what we're trying to do and would like to get involved, drop us a line! To connect with over four thousand debaters of our community, join our facebook group. Our socials can be found below.
</p> 
