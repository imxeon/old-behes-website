  <h1>WHAT IS BEHES ?</h1>
  <p>
 Behes is a non-profit initiative working to promote debate, public speaking and reasoned expression in schools and colleges throughout India and beyond. We are committed to the vision of building an all-India platform for young students to express themselves and debate some engaging and relevant topics.

Every Behes tournament is a celebration of ideas, with students exploring a wide variety of topics and critically examining them. Designed to be interactive and challenging, each tournament rewards the top teams with qualification to the Maha Behes, our season finals held at the end of every calendar year.
</p>
<p>
All the debate topics at our events are given impromptu and based on a suggested reading (and viewing) list that is updated every few months. Students explore selections spanning a variety of disciplines in their bid to challenge the best in the debating community.
The free-flowing debate format encourages growth of research, collaborative and analytical apart from oratory skills.
Behes has now made its way into 22 cities in the 2019 season with over four thousand lives impacted, some of them as young as nine years old.
</p>
<p>
We also offer workshops at gracious host schools (usually preceding tournaments) where we demonstrate How to 'Behes'. If you like what we're trying to do and would like to get involved, drop us a line! To connect with over four thousand debaters of our community, join our <a href='https://www.facebook.com/groups/behesopengroup/'>facebook group</a>.

</p> 
