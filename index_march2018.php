  <h1>WHAT IS BEHES ?</h1>
  <p>
 Behes is a non-profit organization working to promote debate, public speaking and reasoned expression in schools and colleges throughout India and beyond. We are committed to the vision of building a global platform for young students to express themselves and debate some engaging and relevant topics.
</p> 
<p>
Every Behes tournament is a celebration of ideas, with students exploring a wide variety of topics and critically examining them. Designed to be interactive and challenging, each tournament rewards the top teams with qualification to the Maha Behes, our national season finale held in the National Capital Region (NCR) every Christmas.
</p>
<p>All the debate topics at our events are given impromptu and based on a suggested reading (and viewing) list that is updated every few months. Students explore selections spanning a variety of disciplines in their bid to challenge the best in the debating community.</p>
    <p>The free-flowing debate format encourages growth of research, collaborative and analytical apart from oratory skills. </p>
<p>Behes has now made its way into nine cities in the 2017 season with over three thousand lives impacted, some of them as young as nine years old.</p>
   <p>
      We also offer workshops at gracious host schools (usually preceding tournaments) where we demonstrate How to 'Behes'. If you like what we're trying to do and would like to get involved, drop us a line! To connect with over two thousand debaters of our community, join our <a href="https://www.facebook.com/groups/behesopengroup/" style="text-decoration:none" target="_blank"><strong>facebook group</strong></a>.
   </p>
  

